    private void showOpenFileDialog() {
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos de texto", "txt");
        chooser.setFileFilter(filter);
        chooser.showOpenDialog(null);
        if (chooser.getSelectedFile() != null) {
            File f = chooser.getSelectedFile();
            currentFile = chooser.getSelectedFile();
            LBLStatus.setText("Nome do arquivo selecionado - " + f.getName() + "\n"
                    + " - Caminho do arquivo selecionado - " + f.getPath());
            String text = "";
            try {
                FileReader reader = new FileReader(f.getPath());
                int character;

                while ((character = reader.read()) != -1) {
                    text = text + ((char) character);
                }
                reader.close();

            } catch (IOException e) {
                e.printStackTrace();
            }
            TACodigo.setText(text);
            TAMensagens.setText("");
        }
    }

    private void showSaveFileDialog() {
        String editor = TACodigo.getText();
        JFileChooser chooser = new JFileChooser();
        if (currentFile == null) {
            int retrival = chooser.showSaveDialog(null);
            if (retrival == JFileChooser.APPROVE_OPTION) {
                currentFile = new File(chooser.getSelectedFile().getAbsolutePath() + ".txt");
                try (FileWriter fw = new FileWriter(currentFile)) {
                    fw.write(editor);
                    LBLStatus.setText("Arquivo salvo com sucesso");
                    TAMensagens.setText("");
                } catch (Exception ex) {
                    ex.printStackTrace();
                    TAMensagens.setText("Ocorreu um erro ao salvar o arquivo");
                }
            }
        } else {
            chooser.setSelectedFile(currentFile);
            try (FileWriter fw = new FileWriter(chooser.getSelectedFile())) {
                fw.write(editor);
                TAMensagens.setText("");
            } catch (Exception ex) {
                ex.printStackTrace();
                TAMensagens.setText("Ocorreu um erro ao salvar o arquivo");
            }
        }
    }

    private void compile() {
        Lexico lexico = new Lexico();
        Sintatico sintatico = new Sintatico();
        Semantico semantico = new Semantico();

        lexico.setInput(TACodigo.getText());

        try {
            sintatico.parse(lexico, semantico); //Tradução dirigida pela sintaxe

            String newName = currentFile.getName();
            newName = newName.substring(0, newName.lastIndexOf("."));
            newName += ".il";

            var outputFile = new File(currentFile.getParentFile(), newName);

            try (BufferedWriter writer = Files.newBufferedWriter(outputFile.toPath(), StandardCharsets.UTF_8)) {
                for (String line : semantico.getCodigo()) {
                    writer.write(line);
                    writer.newLine();
                }
            } catch (IOException e) {
                e.printStackTrace();
                TAMensagens.setText(e.toString());
            }

            TAMensagens.setText("Programa compilado com sucesso");
        } catch (LexicalError e) {
            int line = e.getLine(TACodigo.getText());
            String msg = "Erro na linha " + line + " - " + e.getCulprit();
            if (!e.getCulprit().isBlank()) {
                msg += " ";
            }
            TAMensagens.setText(msg + e.getMessage());
        } catch (SyntaticError e) {
            String lexeme = sintatico.getCurrentToken().getLexeme();
            int line = e.getLine(TACodigo.getText());
            if (lexeme.equals("$")) {
                lexeme = "EOF";
            }
            TAMensagens.setText("Erro na linha " + line + " - encontrado " + lexeme + " " + e.getMessage());
        } catch (SemanticError e) {
            int line = e.getLine(TACodigo.getText());
            TAMensagens.setText("Erro na linha " + line + " - " + e.getMessage());
        }
    }

